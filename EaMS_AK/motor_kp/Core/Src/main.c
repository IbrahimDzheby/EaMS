void adc_process(){
	  // Начало преобразования в АЦП1 для Encoder
	   HAL_ADC_Start(&hadc1);
	   // Ожидание окончания преобразования
	   HAL_ADC_PollForConversion(&hadc1, 1);
	   // Считывание кода с АЦП
	   ADC_RES = HAL_ADC_GetValue(&hadc1);
	   // Остановка работы АЦП
	   HAL_ADC_Stop(&hadc1);

	   if ((state_motor.r_safety_belt == 0 | state_motor.d_safety_belt == 0)&& speed == 0){
		   speed = 0;
		   TIM8->CCR1=speed;
	   }
	   else if (state_motor.d_mode == 1){
	   	   speed = ADC_RES/20.475;
	   	   rpm = 1000 + speed*30;
	   	   TIM8->CCR1=speed*20.475;
	   }else if (state_motor.r_mode == 1){
		   speed = ADC_RES/68.25;
		   rpm = 1000 + speed*100;
		   TIM8->CCR1=speed*20.475;
	   }else if (state_motor.p_mode == 1){
		   speed = 0;
		   if (state_motor.engine){
			   rpm = 1000;
		   }
		   else if (state_motor.engine == 0){
			   rpm = 0;
		   }
		   TIM8->CCR1=speed;
	   }
	   else {
		   TIM8->CCR1=speed;
	   }


	// Начало преобразования в АЦП1 для Encoder
	HAL_ADC_Start(&hadc2);
	// Ожидание окончания преобразования
	HAL_ADC_PollForConversion(&hadc2, 1);
	// Считывание кода с АЦП
	ADC_RES = HAL_ADC_GetValue(&hadc2);
	// Остановка работы АЦП
	HAL_ADC_Stop(&hadc2);

	rasst = (21.43*4095)/(3.3*ADC_RES);

	if (rasst <=50 && rasst > 30){
		TIM1->PSC= 255;
		TIM1->ARR = 62499;
	}else if (rasst <= 30 && rasst > 15){
		TIM1->PSC= 127;
		TIM1->ARR = 62499;
	}else if (rasst <= 15 && rasst > 0){
		TIM1->PSC= 31;
		TIM1->ARR = 49999;
	}else {
		TIM1->PSC= 0;
		TIM1->ARR = 0;
	}
}


